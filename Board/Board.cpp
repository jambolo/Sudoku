bool Board::consistent(std::vector<int> const & group) const
{
    int values = 0;
    for (int i : group)
    {
        if (!consistent(get(i), values))
            return false;
    }
    return true;
}

bool Board::consistent(int x, int & values)
{
    if (x == EMPTY)
        return true;

    int mask = 1 << x;
    if ((values & mask) != 0)
        return false;

    values |= mask;
    return true;
}
